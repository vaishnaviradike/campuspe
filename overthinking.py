# overthinking-meter.py
print("Welcome to my overthinking world hehe! Let's see how much you're overanalyzing that conversation. ")
thoughts = int(input("How many times did you replay that conversation? "))

if thoughts > 10:
    print("Bestie... you're making a mountain out of a molehill Take a deep breath and let it go.")
elif thoughts > 5:
    print("Hmm. Living rent-free in your head, huh?")
else:
    print("Healthy brain. We love that for you.")